package com.yikuyi.document.vo;

import com.yikuyi.document.model.DocumentLog;

public class DocumentLogVo extends DocumentLog{
	
	private static final long serialVersionUID = -1167516621506701701L;
	//存储发送MQ数量的过程记录的cache
	private Integer countCacheList;
	//文档是否取消，true已取消，false未取消
	private Boolean isCancel;
	
	/**
	 * 文件行数
	 */
	private Integer dataSize;
	
	public Integer getCountCacheList() {
		return countCacheList;
	}
	public void setCountCacheList(Integer countCacheList) {
		this.countCacheList = countCacheList;
	}
	public Boolean getIsCancel() {
		return isCancel;
	}
	public void setIsCancel(Boolean isCancel) {
		this.isCancel = isCancel;
	}
	
	public Integer getDataSize() {
		return dataSize;
	}
	public void setDataSize(Integer dataSize) {
		this.dataSize = dataSize;
	}
}
