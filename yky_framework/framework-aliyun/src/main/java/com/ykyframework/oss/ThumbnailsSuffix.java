package com.ykyframework.oss;

public enum ThumbnailsSuffix {
	THUMBNAIL_SUFFIX_24("@24h_24w_0e"), 
	THUMBNAIL_SUFFIX_32("@32h_32w_0e"), 
	THUMBNAIL_SUFFIX_64("@64h_64w_0e"),
	THUMBNAIL_SUFFIX_120("@120h_120w_0e"),
	THUMBNAIL_SUFFIX_128("@128h_128w_0e");

	String suffix = null;

	private ThumbnailsSuffix(String suffix) {
		this.suffix = suffix;
	}

	public String getSuffix() {
		return suffix;
	}

	@Override
	public String toString() {
		return suffix;
	}
}