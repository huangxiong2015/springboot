package com.ykyframework.oss;

/**
 * 阿里云OSS访问账号相关设置
 * @author liudian@yikuyi.com
 * @version 1.0.0
 */
public class AliyunOSSAccount {
	
	private String accessKeyId = null;
	
	private String accessKeySecret;
	
	private String role;
	
	public AliyunOSSAccount(String accessKeyId, String accessKeySecret,  String roleARN){
		this.accessKeyId = accessKeyId;
		this.accessKeySecret = accessKeySecret;
		this.role = roleARN;
	}

	/**
	 * @return the accessKeyId
	 */
	public final String getAccessKeyId() {
		return accessKeyId;
	}

	/**
	 * @param accessKeyId the accessKeyId to set
	 */
	public final void setAccessKeyId(String accessKeyId) {
		this.accessKeyId = accessKeyId;
	}

	/**
	 * @return the accessKeySecret
	 */
	public final String getAccessKeySecret() {
		return accessKeySecret;
	}

	/**
	 * @param accessKeySecret the accessKeySecret to set
	 */
	public final void setAccessKeySecret(String accessKeySecret) {
		this.accessKeySecret = accessKeySecret;
	}

	/**
	 * @return the role
	 */
	public final String getRole() {
		return role;
	}

	/**
	 * @param role the role to set
	 */
	public final void setRole(String role) {
		this.role = role;
	}
}
